#wap to print the day of the week using function
a=int(input("Enter a number"))
if(a==1):
    print("Monday")
elif(a==2):
    print("Tuesday")
elif(a==3):
    print("Wednesday")
elif(a==4):
    print("Thursday")
elif(a==5):
    print("Friday")
elif(a==6):
    print("saturday")
elif(a==7):
    print("sunday")
else:
    print("Invalid input!!!!!!")